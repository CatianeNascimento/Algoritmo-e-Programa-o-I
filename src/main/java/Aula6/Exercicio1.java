package Aula6;

/**Nesta aula aprendemos a utilizar a estrutura de repetição while.
 *
 * @author Catiane
 */
public class Exercicio1 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       int i=0; 
       
       while ( i <= 10){
           System.out.println("Linha " + i);
           i++;
       }
    }
    
}
